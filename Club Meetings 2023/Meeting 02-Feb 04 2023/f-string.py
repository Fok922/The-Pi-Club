#f-string

temp=28
humidity=80

print('Using f-string')
print(f"Temp : {temp} deg C")
print(f"Humidity : {humidity} %")
print("")
print("")


print('Using concactenantion')
print('Temp : ' + str(temp) + ' deg C')
print('Humidity : ' + str(humidity) + ' %')
print("")
print("")

print('Advantage of f-string')
print("Don't need to convert number value to string")



